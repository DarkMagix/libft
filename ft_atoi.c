#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		l;
	long	n;

	i = 0;
	n = 0;
	l = 0;
	while (str[i] == '\r' || str[i] == '\n' || str[i] == '\t' ||
			str[i] == '\f' || str[i] == '\v' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
		l = i++;
	while (str[i] != '\0')
	{
		if (n >= 922337203685477580 && (((str[i] - '0') > 8 && str[l] == '-') ||
					((str[i] - '0') > 7 && str[l] != '-')))
			return (str[l] == '-' ? 0 : -1);
		if (str[i] >= '0' && str[i] <= '9')
			n = n * 10 + (str[i] - '0');
		else if (!(str[i] >= '0' && str[i] <= '9'))
			break ;
		i++;
	}
	return (str[l] == '-' ? (n * -1) : n);
}
